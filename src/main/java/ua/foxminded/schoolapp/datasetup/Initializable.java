package ua.foxminded.schoolapp.datasetup;

/**
 * The Initializable interface represents a contract for initializing a specific
 * component. Classes that implement this interface should provide an
 * implementation for the initialize() method to perform the initialization
 * logic.
 *
 * @author Serhii Bohdan
 */
public interface Initializable {

    /**
     * Performs initialization.
     */
    void initialize();

}
